public class while1 {
    public static void main(String[] args) {
        java.util.Scanner kb = new java.util.Scanner(System.in);

        int n;
        System.out.println("bir sayı giriniz :");
        n = kb.nextInt();

        while (n > 0){
            System.out.println("bir sayı giriniz :");
            n = kb.nextInt();

        }
    }
}

